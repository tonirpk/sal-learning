let key = "Enter"
switch (key) {
    case "Enter":
        console.log("Enter key was pressed")
        break;
    case "Shift":
        console.log("Shift key was pressed")
        break;
    case "Alt":
        console.log("Alt key was pressed")
        break;
    case "Ctrl":
        console.log("Ctrl key was pressed")
        break;
    default:
        console.log("Nothing was pressed")
}